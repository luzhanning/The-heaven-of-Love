package com.example.demo.model;

public class Chuangguan {

    private String nickname;
    private Integer npc;
    private Integer jack;
    private Integer feifeifei;

    public String getNickname() {
        return nickname;
    }

    public void setNickname(String nickname) {
        this.nickname = nickname;
    }

    public Integer getNpc() {
        return npc;
    }

    public void setNpc(Integer npc) {
        this.npc = npc;
    }

    public Integer getJack() {
        return jack;
    }

    public void setJack(Integer jack) {
        this.jack = jack;
    }

    public Integer getFeifeifei() {
        return feifeifei;
    }

    public void setFeifeifei(Integer feifeifei) {
        this.feifeifei = feifeifei;
    }
}
